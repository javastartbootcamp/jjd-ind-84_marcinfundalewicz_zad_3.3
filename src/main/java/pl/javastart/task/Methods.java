package pl.javastart.task;

public class Methods {

    boolean isEven(int number) {
        return number % 2 == 0;
    }

    boolean isOdd(int number) {
        return number % 2 != 0;
    }

    double circleField(double radius) {
        return radius * radius * 3.14;
    }

    int power(int number) {
        return number * number;
    }
}
